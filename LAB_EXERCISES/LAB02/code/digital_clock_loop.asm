ORG 0000H
LJMP MAIN

ORG 0100H

MAIN:
RESET_CLOCK:
    MOV R1, #0
    MOV R2, #0
    MOV R3, #0
    MOV R4, #0
    MOV R5, #0
    MOV R6, #0

HIENTHI:
    MOV R0, #167
LOOP_QUET:
    MOV A, R6
    ORL A, #00010000B
    MOV P2, A
    MOV P3, #00100000B
    ACALL DELAY_1MS_LOOP
    MOV P3, #0

    MOV A, R5
    ORL A, #00010000B
    MOV P2, A
    MOV P3, #00010000B
    ACALL DELAY_1MS_LOOP
    MOV P3, #0

    MOV P2, R4
    MOV P3, #00001000B
    ACALL DELAY_1MS_LOOP
    MOV P3, #0

    MOV A, R3
    ORL A, #00010000B
    MOV P2, A
    MOV P3, #00000100B
    ACALL DELAY_1MS_LOOP
    MOV P3, #0

    MOV P2, R2
    MOV P3, #00000010B
    ACALL DELAY_1MS_LOOP
    MOV P3, #0

    MOV A, R1
    ORL A, #00010000B
    MOV P2, A
    MOV P3, #00000001B
    ACALL DELAY_1MS_LOOP
    MOV P3, #0

    DJNZ R0, LOOP_QUET

TIME:
    INC R6
    CJNE R6, #10, HIENTHI
    MOV R6, #0

    INC R5
    CJNE R5, #6, HIENTHI
    MOV R5, #0

    INC R4
    CJNE R4, #10, HIENTHI
    MOV R4, #0

    INC R3
    CJNE R3, #6, HIENTHI
    MOV R3, #0

    INC R2

    CJNE R1, #2, CHECK_24H
    CJNE R2, #4, CHECK_24H
    LJMP RESET_CLOCK

CHECK_24H:
    CJNE R2, #10, HIENTHI
    MOV R2, #0
    INC R1
    LJMP HIENTHI

DELAY_1MS_TIMER:
    MOV TMOD, #01H
    MOV TH0, #0FCH
    MOV TL0, #018H
    CLR TF0
    SETB TR0
    JNB TF0, $
    CLR TR0
    RET

DELAY_1MS_LOOP:
    PUSH 00H
    PUSH 01H
    MOV R0, #2
LOOP1:
    MOV R1, #100
CONT:
    DJNZ R1, LOOP2
    DJNZ R0, LOOP1
    POP 01H
    POP 00H
    RET
LOOP2:
    NOP
    JMP CONT

END
